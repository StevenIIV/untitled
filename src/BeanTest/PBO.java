package BeanTest;

public class PBO {
    private int s1;
    private String s;

    public PBO(int s1, String s) {
        this.s1 = s1;
        this.s = s;
    }

    public int getS1() {
        return s1;
    }

    public String getS() {
        return s;
    }

    public void setS1(int s1) {
        this.s1 = s1;
    }

    public void setS(String s) {
        this.s = s;
    }
}
